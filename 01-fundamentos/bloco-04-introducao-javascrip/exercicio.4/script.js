let aluno = "lista";

switch (aluno) {
  case "aprovado":
    console.log("Parabéns, você foi aprovada(0)!");
    break;

  case "lista":
    console.log("Você está na nossa lista de espera");
    break;

  case "reprovado":
    console.log("Você foi reprovado(0)");
    break;

  default:
    console.log("Informação incorreta");
}
